#' Joint DVI search
#'
#' `dviJoint()` performs a joint likelihood search over candidate assignments in
#' a DVI problem, ranking all assignments and returning both PM-oriented and
#' AM-oriented assignment columns. It is used internally by [dviSolve()] for
#' unresolved complex families. Compared with the legacy [jointDVI()], it does 
#' not perform preprocessing steps such as exclusions or undisputed matches.
#'
#' @param dvi A `dviData` object, typically created with [dviData()].
#' @param assignments A data frame containing the assignments to be considered
#'   in the joint analysis. By default, this is automatically generated by
#'   taking all combinations from `dvi$pairings`.
#' @param disableMutations Either TRUE, FALSE (default) or NA. If NA, mutation
#'   modelling is applied only in families where the reference data are
#'   incompatible with the pedigree unless at least one mutation has occurred.
#' @param ignoreSex A logical, only relevant if `dvi$pairings` is NULL, so that
#'   candidate pairings have to be generated.
#' @param maxAssign A positive integer. If the number of assignments going into
#'   the joint calculation exceeds this, the function will abort with an
#'   informative error message. Default: 1e5.
#' @param numCores Deprecated and ignored.
#' @param cutoff Currently ignored.
#' @param verbose A logical.
#' @param progress A logical, indicating if a progress bar should be shown.
#'
#' @return A data frame.
#' @examples
#' dviJoint(example2)
#'
#' @export
dviJoint = function(dvi, assignments = NULL, ignoreSex = FALSE, disableMutations = FALSE, 
                    maxAssign = 1e5, numCores = 1, cutoff = 0, verbose = TRUE, progress = interactive()) {
  
  if(!is.null(numCores) && numCores != 1)
    warning("`numCores` is deprecated and currently ignored", call. = FALSE)
  
  verbose = if(isTRUE(verbose)) c("input", "status", "details") else if(isFALSE(verbose)) character(0) else verbose
  show = function(type, expr) {if(type %in% verbose) force(expr)}
  
  # Ensure proper dviData object
  dvi = consolidateDVI(dvi)
  
  show("input", print.dviData(dvi))
  
  ### Mutation disabling
  if(any(allowsMutations(dvi$am))) {
    am = dvi$am
    
    mutAction = if (isTRUE(disableMutations)) 2 else if (identical(disableMutations, NA)) 1 else 0
  
    show("input", cat(sprintf("\nMutation modelling: %s\n",
      c("Enable everywhere", "Enable only in inconsistent families", "Disable everywhere")[mutAction + 1])))
  
    disableFams = NULL
  
    if (mutAction == 2) {
      disableFams = seq_along(am)
    }
    else if (mutAction == 1) {
      am.nomut = setMutmod(am, model = NULL)
      incons = vapply(am.nomut, loglikTotal, FUN.VALUE = 1) == -Inf
      badFams = which(incons)
      disableFams = which(!incons)
      
      show("status", {
        nbd = length(badFams)
        ndf = length(disableFams)
        if(nbd == 0)
          cat("No inconsistent families; all mutation models disabled\n")
        else {
          famLabs = names(am) %||% seq_along(am)
          cat("Inconsistent families: ", trunc(famLabs[badFams]), "\n")
          cat("Disabling mutation models in remaining", ndf, if(ndf == 1) "family" else "families", "\n")
        }
      })
    }
    
    if(length(disableFams)) {
      am[disableFams] = setMutmod(am[disableFams], model = NULL)
    }
    
    # Update DVI object
    dvi$am = am
  }
  
  pm = dvi$pm 
  am = dvi$am
  vics = names(pm)
  pairings = dvi$pairings %||% generatePairings(dvi, ignoreSex = ignoreSex)
  
  # Find (or estimate) the number of assignments
  if(is.null(assignments)) 
    nAss = dviGridSize(pairings = pairings, max = maxAssign)
   else 
    nAss = list(size = nrow(assignments), type = "exact")

  if(nAss$size > maxAssign)
    stop2(sprintf("Number of assignments (%s: %g) exceeds max (%d)", nAss$type, nAss$size, maxAssign))
  show("status", cat(sprintf("Number of assignments: %g (%s)\n", nAss$size, nAss$type)))
    
  if(nAss$size == 0)
    stop2("No assignments")
  
  if(is.null(assignments))
    assignmentMat = expand.grid.nodup(pairings, max = maxAssign, df = FALSE)
  else {
    if(!.mysetequal(names(assignments), vics))
      stop2("Names of supplied assignments do not match `pm` names")
    # Ensure order and convert to matrix
    assignmentMat = as.matrix(assignments[vics])
  }

  # Actual number of assignments
  nAss = nrow(assignmentMat)
  
  # Initial loglikelihoods
  logliks.PM = vapply(pm, loglikTotal, FUN.VALUE = 1)
  logliks.AM = vapply(am, loglikTotal, FUN.VALUE = 1)
  
  loglik0 = sum(logliks.PM) + sum(logliks.AM)
  if(loglik0 == -Inf)
    stop2("Impossible initial data: AM component ", which(logliks.AM == -Inf))
  
  # Main calculation
  if(progress) {
    pb = utils::txtProgressBar(min = 0, max = nAss, style = 3)
    on.exit(close(pb), add = TRUE)
  }
  
  loglik = vapply(seq_len(nAss), function(i) {
    if(progress)
      utils::setTxtProgressBar(pb, i)
    loglikAssign(pm, am, vics, assignmentMat[i, ], loglik0, logliks.PM, logliks.AM)
  }, numeric(1))

  # Use data frame for sorting and final table
  assignments = as.data.frame(assignmentMat)
  
  # Sort in decreasing likelihood, break ties with assignments
  g = assignments
  g[g == "*"] = NA
  ORD = do.call(order, c(list(-loglik), g))
  
  assignments = assignments[ORD, , drop = FALSE]
  rownames(assignments) = NULL
  loglik = loglik[ORD]
  
  # LR relative to the best solution
  LR = exp(loglik[1] - loglik)
  
  # Skip posteriors for now
  # posterior = LR/sum(LR) # assuming flat prior
  
  # AM-centric assignment table
  assignments2 = swapOrientation(assignments, from = vics, to = dvi$missing)
  
  # Collect joint results, including both PM and AM centric assignment tables
  cbind(assignments, loglik = loglik, LR = LR, assignments2)
}


#' Swap orientation of an assignment table
#'
#' This function switches the roles of victims and missing persons in a table of
#' assignments, from PM-oriented (victims as column names) to AM-oriented (missing
#' persons as column names), and _vice versa_. In both versions, each row
#' describes the same assignment vector.
#'
#' @param df A data frame. Each row is an assignment, with `*` representing
#'   non-pairing.
#' @param from A character vector; either victims or missing persons. By
#'   default, the column names of `df`. The only time this argument is needed,
#'   is when `df` has other columns in addition, as in output tables of
#'   `dviJoint()`.
#' @param to The column names of the transformed data frame. If missing, the
#'   unique elements of `df` are used. An error is raised if `to` does not
#'   contain all elements of `df` (except `*`).
#'
#' @return A data frame with `nrow(df)` rows and `length(to)` columns.
#'
#' @examples
#' df = example1 |> generatePairings() |> expand.grid.nodup()
#' df
#' swapOrientation(df)
#' 
#' # Swap is idempotent
#' stopifnot(identical(swapOrientation(swapOrientation(df)), df))
#' 
#' @export
swapOrientation = function(df, from = NULL, to = NULL) {

  if(is.null(from))
    from = names(df)
  
  # Matrix for speed
  amat = as.matrix(df[from])
  
  if(is.null(to))
    to = setdiff(sort.default(unique.default(amat)), "*")
  
  dims = dim(amat)
  
  # Match
  toExt = c(to, "*")
  idx = match(amat, toExt, nomatch = 0L)
  if(any(idx == 0))
     stop2("Table entry not included in `to`: ", setdiff(amat, toExt))
  idx[idx == length(toExt)] = 0L
  dim(idx) = dims
  
  # Melt to long format
  long = cbind(which(idx > 0, arr.ind = TRUE), 
               val = idx[idx > 0])
  
  # Create result matrix
  new = matrix("*", nrow = dims[1], ncol = length(to), dimnames = list(NULL, to))
  
  # Pivot back to wide
  new[long[, c(1,3)]] = from[long[,2]]
  
  res = as.data.frame(new)
  if(ncol(df) > length(from))
    res = cbind(res, df[!names(df) %in% from])
  res
}

